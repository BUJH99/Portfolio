 * Description  : 프레임 단위 누적(Q11.16) 후 TLAST 시 반올림·포화 적용하여 Q5.11로 내보내는 AXIS 스타일 누산기
 * Design Notes : 입력은 Q0.16로 들어오며 상위 11비트 제로확장해 누적 범위 확보, 출력 정지 시 백프레셔로 입력 차단
 * Dependencies : AXI-Stream 호환 상위 모듈(Valid/Ready/Last 제어), 다운스트림 소비자(iReady)
 * =====================================================================================================================*/

/**
 * @brief   TLAST에서만 결과를 커밋하는 프레임 누산기(Q0.16 → Q11.16 → Q5.11).
 * @details 데이터는 TVALID/TREADY가 동시에 참일 때만 수용하며, 프레임 종료 비트(iLast)에서 반올림(+16) 후 >>5로
 *          Q11.16을 Q5.11로 축소하고, 범위를 넘으면 0xFFFF로 포화해 다운스트림 오버플로 영향 최소화.
 *          출력이 소비되지 않으면(oValid=1, iReady=0) oReady가 0이 되어 입력 흐름을 정지시키는 백프레셔를 형성.
 * @param   iClk  상승엣지 동기 클럭; 단일 클럭 도메인 가정으로 메타 안정화 부담을 제거
 * @param   iRsn  비동기 저활성 리셋; 전역 초기화와 빠른 복귀를 위해 네거티브 액티브 선택
 * @param   iValid  입력 유효 플래그; TVALID로 취급하여 fire 생성에 참여
 * @param   oReady  입력 수용 가능 표시; 출력 홀드 중엔 0이 되어 상류 백프레셔 보장
 * @param   iLast   프레임 종료 비트; 이 사이클의 누적 합을 결과로 커밋하는 트리거로 사용
 * @param   iData   16비트 Q0.16 데이터; 정수부 0비트로 누적 시 상위 제로확장이 안전
 * @param   oValid  결과 유효 플래그; TLAST에서만 1로 올라가며 소비되기 전까지 유지
 * @param   iReady  결과 소비 준비; 다운스트림 흐름 제어 신호로 백프레셔 경로 형성
 * @param   oData   16비트 Q5.11 결과; 반올림·포화 후 프레임 단위로만 업데이트
 */

 /**********************************************************************
  * Stage 0: 인터페이스 선언
  * - AXIS 호환 신호 집합으로 상·하류와 명확한 백프레셔 경로 보장
  **********************************************************************/
module Adder_block ( // 프레임 경계에서만 결과를 내보내 누적 오차 누수와 타이밍 경쟁을 회피
    input  wire         iClk, // 단일 클럭 도메인 기준 동기화로 시간 폐루프 단순화
    input  wire         iRsn, // 네거티브 액티브 비동기 리셋으로 전역 초기화 지연 최소화

    // --- 입력 인터페이스 (AXI-Stream Slave 스타일) ---
    input  wire         iValid, // TVALID; oReady와 동시 참일 때만 데이터 수용해 샘플 손실 방지
    output wire         oReady, // 상류 허용; 출력이 홀드되면 0으로 만들어 백프레셔 전파
    input  wire         iLast,  // 프레임 트레일러; 이 사이클의 합을 결과로 커밋하는 경계 신호
    input  wire [15:0]  iData,  // Q0.16; 정수부 0으로 누적기 상단 비트 여유를 확보

    // --- 출력 인터페이스 (AXI-Stream Master 스타일) ---
    output wire         oValid, // 결과 유효; TLAST에서만 1로 세트되어 다운스트림 동기화 용이
    input  wire         iReady, // 결과 소비 준비; 0일 때 입력 수용을 막아 버퍼 오버런 방지
    output wire [15:0]  oData   // Q5.11; 반올림(+16) 후 >>5, 포화 포함으로 안전한 다운스케일
); // I/O 폭과 타이밍은 정해진 Q포맷과 AXIS 핸드셰이크 요구에 맞춤

    /**********************************************************************
     * Stage 1: 상태 정의와 고정 상수
     * - 1비트 FSM으로 합산 중/유휴만 관리해 타이밍 단순화
     **********************************************************************/
    localparam ST_IDLE    = 1'b0; // 유휴 상태; 첫 샘플에서 누적기 시드로 직접 반영해 불필요 연산 제거
    localparam ST_SUMMING = 1'b1; // 누적 상태; TLAST 도달 시 결과 커밋 후 유휴 복귀로 프레임 분리 명확화

    // 변환 상수: Q11.16 -> Q5.11
    localparam integer SHIFT_FRAC  = 5;      // 소수부 16→11 축소량; 라운딩 전 보정치 크기를 정의
    localparam [26:0]  ROUND_CONST = 27'd16; // +2^(SHIFT_FRAC-1); half-up 라운딩으로 양·음 편향 최소화

    /**********************************************************************
     * Stage 2: 상태/데이터 레지스터
     * - 누적은 27b(Q11.16)로 정수부 여유(11b)를 확보해 포화 전 영역 확대
     **********************************************************************/
    reg        rState; // FSM 현재 상태; 단순 2상태로 합산 흐름만 관리해 결정 논리 최소화
    reg [26:0] rSumAcc; // Q11.16 누적기; 프레임 내 합산 중간값을 유지해 TLAST 시 즉시 사용
    reg [15:0] rDataOut; // Q5.11 결과 래치; oValid 유지 동안 데이터 안정화
    reg        rValidOut; // 결과 유효 래치; 소비 시점까지 유지해 다운스트림과 안전한 핸드셰이크

    /**********************************************************************
     * Stage 3: 핸드셰이크 및 데이터 확장
     * - fire 펄스로 샘플 수용을 단일 조건으로 캡슐화
     **********************************************************************/
    wire fire = iValid && oReady; // TVALID/TREADY 동시 참일 때만 샘플 수용해 중복·누락 방지

    // 입력 확장: Q0.16 → Q11.16 (상위 11비트 제로 확장)
    wire [26:0] wDataExtended  = {11'b0, iData}; // 정수부 여유를 제로로 채워 누적 시 오버런 여지 확보

    // 누적 합(이번 샘플 반영)
    wire [26:0] wTransactionSum =
        (rState == ST_IDLE) ? wDataExtended : (rSumAcc + wDataExtended); // 첫 샘플은 시드로 사용해 불필요 가산 피크 제거

    // Q11.16 → Q5.11 : 반올림(+16) 후 >>5
    wire [26:0] wRoundAdd     = wTransactionSum + ROUND_CONST; // half-up 라운딩으로 절사 오차 평균 편향 완화
    wire [21:0] wShifted      = wRoundAdd[26:5]; // 상위 비트 선택으로 산술 시프트 비용 없이 포맷 축소
    wire        overflow_q511 = |wShifted[21:16]; // 16비트 범위 초과 감지로 포화 트리거
    wire [15:0] wDataOutQ511  = overflow_q511 ? 16'hFFFF // 포화로 다운스트림 계산의 안전 범위 유지
                                              : wShifted[15:0]; // 범위 내면 손실 최소화해 전달

    /**********************************************************************
     * Stage 4: 다음 상태 결정(콤비)
     * - TLAST가 수용된 사이클(fire)에서만 IDLE 복귀로 경계 명확화
     **********************************************************************/
    reg rNextState; // 콤비 상태 변수; 등록 전 안정적 전이 계산
    always @(*) begin // 조합 논리로 경로 지연 최소화
        rNextState = rState; // 기본 유지로 글리치 방지
        case (rState) // 2상태 FSM으로 디코딩 복잡도 축소
            ST_IDLE:    if (fire)            rNextState = ST_SUMMING; // 첫 유효 샘플 수용 시 합산 모드 진입으로 불필요 대기 제거
            ST_SUMMING: if (fire && iLast)   rNextState = ST_IDLE; // TLAST 수용된 그 사이클에 프레임 종료 보장
        endcase // 상태별 전이를 최소화해 타이밍 경로 단축
    end // 콤비 상태 계산 종료

    /**********************************************************************
     * Stage 5: 순차 로직(레지스터 업데이트)
     * - 출력 홀드 시 입력 차단, TLAST에서 결과 커밋과 누적기 클리어를 동시 수행
     **********************************************************************/
    always @(posedge iClk or negedge iRsn) begin // 상승엣지 샘플링, 비동기 저활성 리셋로 빠른 초기화
        if (!iRsn) begin // 전역 초기화 시 안정 상태 강제
            rState    <= ST_IDLE; // 유휴 진입으로 다음 첫 샘플을 시드로 사용 가능
            rSumAcc   <= 27'b0; // 누적기 초기화로 랜덤 파워온 값 제거
            rValidOut <= 1'b0; // 유효 플래그 초기화로 스푸리어스 출력 방지
            rDataOut  <= 16'b0; // 데이터 초기화로 X 전파 차단
        end else begin // 정상 동작 구간
            rState <= rNextState; // 한 사이클 지연의 동기 FSM로 헤이저드 방지

            // 출력 소비되면 valid 낮춤
            if (rValidOut && iReady) begin // 다운스트림이 소비 완료했을 때만 플래그 드롭으로 핸드셰이크 일관성 유지
                rValidOut <= 1'b0; // 다음 결과 수용 준비를 위해 유효 클리어
            end // 소비 완료 처리 종료

            if (fire) begin // 유효 입력이 수용된 사이클에서만 상태·데이터 갱신
                // 누적 진행
                rSumAcc <= wTransactionSum; // 중간 합을 즉시 반영해 다음 샘플 대비 준비
                // NOTE: 동일 always 블록 내 rSumAcc 다중 할당은 마지막 할당이 우선 적용됨(논블로킹 규칙 기준).
                //       TLAST 처리에서 0 클리어가 우선되어 프레임 간 누적 누수 방지.

                if (iLast) begin // 프레임 경계에서만 결과 커밋해 파이프라인 일관성 유지
                    // 프레임 종료: 결과 산출 및 valid 세트
                    rDataOut  <= wDataOutQ511; // 반올림·포화 적용된 Q5.11로 결과 안정화
                    rValidOut <= 1'b1; // 결과 존재를 한 사이클 이상 유지해 안전한 소비 허용

                    // 프레임 간 누수 방지
                    rSumAcc   <= 27'd0; // 다음 프레임의 초기 상태를 보장해 오차 전이 차단
                end // TLAST 처리 종료
            end // fire 처리 종료
        end // 정상 동작 종료
    end // 순차 로직 종료

    /**********************************************************************
     * Stage 6: 출력 및 준비 신호
     * - 출력 홀드(oValid=1, iReady=0) 동안 oReady=0으로 상류 입력 차단
     **********************************************************************/
    assign oData  = rDataOut; // 결과는 래치된 값으로 유지되어 다운스트림 샘플링 타이밍 여유 제공
    assign oValid = rValidOut; // 유효 상태를 래치 기반으로 제공해 핸드셰이크 안정화
    assign oReady = !rValidOut || iReady; // 결과 미소비 시 입력 차단으로 버퍼 오버런 방지

endmodule // 프레임 단위 커밋 구조로 경계 조건과 수치 안정성을 동시 확보

